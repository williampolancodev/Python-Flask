# ¿Que es Flask?
"""
    Flask es un microframework de python para desarrollar aplicaciones web. Es un microframework porque permite
    crear aplicaciones pequeñas con poco codigo(incluso solo utilizando un archivo de python), pero tambien sirve 
    para crear aplicaciones tanto medianas y grandes y configurarlas para que sean escalables.

    La razon por la que Flask permite crear aplicaciones pequeñas con muy poco codigo, se debe a que proporciona
    un enrutador de URL y que cuenta con herramientas para manejas las solicitudes http. Ademas, de que Flask es
    flexible a la hora de programar, ya que permite al programador utilizar las librerias y herramientas que el 
    desee y hasta incluso personalizar la configuracion del mismo. Esto es lo que lo hace perfecto para desarrollar
    tanto aplicaciones pequeñas como grandes, ya que al tener la flexibilidad de configurarlo segun lo que necesites
    podras adaptarlo segun el proyecto que estes realizando.

"""

# Ventajas de Flask:
"""
- Facil de usar: Es facil de aprender y de usar, ya que no tiene una gran cantidad de conceptos complicados y permite
comenzar a desarrollar aplicaciones web en poco tiempo y poco codigo. 
- Flexibilidad: Es muy flexible porque permite a los desarrolladores elegir las herramientas y las tecnologias que desean
para su proyecto. Es decir, no impone restricciones en la manera que debe construirle las aplicaciones.
- Pequeño y ligero: Es pequeño y ligero ya que flask viene solo con la herramientas necesarias para crear una aplicacion web,
lo cual permite crear toda una aplicacion web pequeña desde un archivo py. Y a medida que el proyecto se hace más grande se 
pueden agregar nuevas herramientas mediante plugin, asi se puede utilizar para proyectos medianos y grandes.
- Comunidad activa
- Bajo nivel de abstraccion: AL tener bajo nivel de abstraccion permite al desarrollador tener un mayor control sobre la 
aplicacion al poder configurar como tambien instalar la herramientas que vaya a utilizar en su aplicacion.
- Facil de integrar a otros servicios: Debido a que tiene una amplia compatibilidad con diversos servicios.
"""